func TestStatusRows(t *testing.T) {
	breakdown := map[int]int64{200: 80, 500: 20}
	rows := statusRows(breakdown, 100)
	if len(rows) != 2 {
		t.Fatalf("expected 2 rows, got %d", len(rows))
	}
	// Check pct is computed
	for _, row := range rows {
		if row.Pct < 0 || row.Pct > 100 {
			t.Errorf("invalid percentage %f for code %d", row.Pct, row.Code)
		}
	}
}

func TestHistogramRows(t *testing.T) {
	buckets := map[string]int64{"<10ms": 30, "10-50ms": 50, ">50ms": 20}
	rows := histogramRows(buckets, 100)
	if len(rows) != 3 {
		t.Fatalf("expected 3 rows, got %d", len(rows))
	}
}
